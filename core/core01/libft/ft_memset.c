#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*p_s;

	i = 0;
	p_s = s;
	while (i <= n / sizeof(char))
	{
		p_s[i] = c;
		i++;
	}
	return (s);
}
